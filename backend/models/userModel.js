import mongoose from "mongoose";

const userSechema = mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    isAdmin: {
      type: Boolean,
      required: true,
      default: false,
    },
  },
  {
    timesteamps: true,
  }
);

const User = mongoose.model("User", userSechema);

export default User;
